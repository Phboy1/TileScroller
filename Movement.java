public class Movement {
    int playerX;
    int playerY;
    int cameraX;
    int cameraY;

    Movement (int playerX, int playerY, int cameraX, int cameraY)
    {
        this.playerX = playerX;
        this.playerY = playerY;
        this.cameraX = cameraX;
        this.cameraY = cameraY;
    }
}
